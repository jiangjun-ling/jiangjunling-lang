# 4. 返回多个值的函数（通过列表）
策 分析成绩(成绩列表)：
    最高分 = 0
    最低分 = 100
    总和 = 0
    
    访 成绩 于 成绩列表：
        若 成绩 > 最高分 则：
            最高分 = 成绩
        若 成绩 < 最低分 则：
            最低分 = 成绩
        总和 = 总和 + 成绩
    
    平均分 = 总和 / 长度(成绩列表)
    返 [最高分, 最低分, 平均分]

令行 分析结果 = 分析成绩([85, 92, 78, 96, 88])
报 "成绩分析 - 最高分:" + 分析结果[0] + ", 最低分:" + 分析结果[1] + ", 平均分:" + 分析结果[2]

