# 3. 数学函数
策 计算阶乘(数)：
    若 数 <= 1 则：
        返 1
    否则：
        返 数 * 计算阶乘(数 - 1)

策 计算平均值(数字列表)：
    总和 = 0
    访 数 于 数字列表：
        总和 = 总和 + 数
    
    若 长度(数字列表) > 0 则：
        返 总和 / 长度(数字列表)
    否则：
        返 0

