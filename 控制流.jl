# 1. 多重条件判断
策 评估成绩(分数)：
    若 分数 >= 90 则：
        返 "优秀"
    否则若 分数 >= 80 则：
        返 "良好"
    否则若 分数 >= 70 则：
        返 "中等"
    否则若 分数 >= 60 则：
        返 "及格"
    否则：
        返 "不及格"

# 测试成绩评估
令 测试成绩 = [95, 85, 75, 65, 55]

报 "=== 成绩评估 ==="
访 分数 于 测试成绩：
    等级 = 评估成绩(分数)
    报 "分数 " + 分数 + " 的等级是: " + 等级

# 2. 嵌套循环
报 "=== 嵌套循环演示 ==="
令：
    班级列表 = ["一班", "二班", "三班"]
    科目列表 = ["语文", "数学", "英语"]

访 班级 于 班级列表：
    报 "=== " + 班级 + " ==="
    访 科目 于 科目列表：
        报 "正在处理 " + 班级 + " 的 " + 科目 + " 成绩"

# 3. 使用范围函数循环
报 "=== 范围循环演示 ==="
报 "计数从 1 到 5:"
访 i 于 范围(1, 6)：
    报 "当前计数: " + i

# 4. 复杂条件组合
策 判断季节(月份)：
    若 (月份 >= 3 且 月份 <= 5) 则：
        返 "春季"
    否则若 (月份 >= 6 且 月份 <= 8) 则：
        返 "夏季"
    否则若 (月份 >= 9 且 月份 <= 11) 则：
        返 "秋季"
    否则：
        返 "冬季"

# 测试季节判断
令 月份列表 = [1, 4, 7, 10]

报 "=== 季节判断 ==="
访 月份 于 月份列表：
    季节 = 判断季节(月份)
    报 月份 + "月是 " + 季节

