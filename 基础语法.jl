# 将军令语言基础语法示例
# 文件名: 基础语法.jl

# 1. 变量声明
令：
    姓名 = "张三"
    年龄 = 20
    成绩列表 = [85, 92, 78, 96]

# 2. 输出语句
报 "=== 基础语法演示 ==="
报 "姓名:" + 姓名
报 "年龄:" + 年龄
报 "成绩列表:" + 成绩列表

# 3. 条件语句
若 年龄 >= 18 则：
    报 姓名 + "是成年人"
否则：
    报 姓名 + "是未成年人"

# 4. 循环语句
报 "=== 循环演示 ==="
访 成绩 于 成绩列表：
    若 成绩 >= 90 则：
        报 "优秀成绩:" + 成绩
    否则若 成绩 >= 80 则：
        报 "良好成绩:" + 成绩
    否则：
        报 "一般成绩:" + 成绩

# 5. 数学运算
令：
    甲 = 15
    乙 = 4
    
令行 和 = 甲 + 乙
令行 差 = 甲 - 乙  
令行 积 = 甲 * 乙
令行 商 = 甲 / 乙

报 "=== 数学运算 ==="
报 "甲 + 乙 = " + 和
报 "甲 - 乙 = " + 差
报 "甲 * 乙 = " + 积
报 "甲 / 乙 = " + 商

# 6. 比较运算
令行 相等 = 甲 == 乙
令行 大于 = 甲 > 乙

报 "甲 == 乙: " + 相等
报 "甲 > 乙: " + 大于

报 "=== 演示完成 ==="